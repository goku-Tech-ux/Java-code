import java.util.*;
public class main{
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        System.out.println("ENTER THE NUMBER OF LINES");
        int n = sc.nextInt();
       for(int i=n;i>=0;i--)
       {
           for(int j=i;j<n;j++)
           {
               System.out.print("* ");
           
       }System.out.println();
    }
}}
